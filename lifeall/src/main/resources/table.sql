CREATE TABLE `news_content` (
  `ID` bigint(20) NOT NULL AUTO_INCREMENT,
  `WEBSITE_ID` bigint(20) NOT NULL COMMENT '爬取网址ID',
  `CRAWL_URL` varchar(500) NOT NULL COMMENT '爬取地址',
  `NEWS_NAME` varchar(200) NOT NULL,
  `NEWS_URL` varchar(500) NOT NULL,
  `NEWS_IMAGE` varchar(500) DEFAULT NULL,
  `NEWS_DESC` text,
  `KEYWORDS` varchar(100) DEFAULT NULL,
  `PUBLISH_TIME` datetime DEFAULT NULL,
  `NEWS_RESOURCE` varchar(50) DEFAULT NULL,
  `NEWS_AUTHOR` varchar(50) DEFAULT NULL,
  `COMMENT_NUM` int(11) DEFAULT NULL,
  `READ_NUM` int(11) DEFAULT NULL,
  `CREATE_TIME` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`ID`),
  UNIQUE KEY `IDX_NEWS_NAME` (`NEWS_NAME`,`NEWS_URL`),
  KEY `IDX_KEYWORDS` (`KEYWORDS`),
  KEY `IDX_PUBLISH_TIME` (`PUBLISH_TIME`),
  KEY `IDX_NEWS_AUTHOR` (`NEWS_AUTHOR`),
  KEY `IDX_NEWS_RESOURCE` (`NEWS_RESOURCE`)
) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8mb4;